/**
 * Demonstrates the use of a while statement. 
 */
public class Count1
{
/**
 * Prints from 1 to 10 using a while loop.
 * @param args Standard command line arguments.
 */
   public static void main(String[] args)
   {
      int count = 1;
      while (count <= 10)
      {
         System.out.println(count);
         count++;
      }
      System.out.println("Done");
   } 
}
